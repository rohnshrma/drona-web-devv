// typeof keyword returns the data type of the given value / variable

// number
console.log(typeof 123); // number
console.log(typeof 1.23); // number

// string
console.log("hello");
console.log("hello");
console.log(`hello`);

// boolean / bool : true | false

// undefined : unintentional absence of value
// variable exists but value doesn't

// null : intentional absence of value

var age = null;

